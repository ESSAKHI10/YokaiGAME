package models;

public class BoardCase {
	
	private int x ;
	private int y ;
	private String Style;
	
	
	
	 
	public String getStyle() {
		return Style;
	}
	public void setStyle(String style) {
		Style = style;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public BoardCase() {
		super();
	}
	

}
